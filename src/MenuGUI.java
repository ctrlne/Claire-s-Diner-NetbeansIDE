    public void startDinerTimer() {
    dinerTimer = new Timer(1000, new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            dinerSecondsElapsed++;
            jLabelTimer.setText("Diner Timer: " + dinerSecondsElapsed + "s");
        }
    });
    dinerTimer.start();
}

    public void stopDinerTimer() {
    if (dinerTimer != null) {
        dinerTimer.stop();
    }
}

public void resetDinerTimer() {
    stopDinerTimer();
    dinerSecondsElapsed = 0;
    jLabelTimer.setText("Diner Timer: 0s");
}

    
