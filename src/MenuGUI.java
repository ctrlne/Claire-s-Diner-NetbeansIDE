    private void displayOrderMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayOrderMenuItemActionPerformed
        StringBuilder order = new StringBuilder();
    int total = 0;

    // Salads
    if (jRadioButton1.isSelected()) {
        order.append("Salad: Chicken Salad\n");
        total += 100;
    } else if (tunaSaladRadioButton.isSelected()) {
        order.append("Salad: Tuna Salad\n");
        total += 150;
    } else if (vegetableSaladRadioButton.isSelected()) {
        order.append("Salad: Vegetable Salad\n");
        total += 200;
    }

    // Drinks
    if (sodaRadioButton.isSelected()) {
        order.append("Drink: Soda\n");
        total += 50;
    } else if (juiceRadioButton.isSelected()) {
        order.append("Drink: Juice\n");
        total += 100;
    } else if (milkTeaRadioButton.isSelected()) {
        order.append("Drink: Milk Tea\n");
        total += 150;
    }



    // Special Request
    String specialRequest = jTextField1.getText().trim();
    if (!specialRequest.isEmpty()) {
        order.append("Special Request: ").append(specialRequest).append("\n");
    }

    if (order.length() == 0) {
        order.append("No selections made.");
    } else {
        order.append("\nTotal Price: ").append(total);
    }

    JOptionPane.showMessageDialog(this, order.toString(),
            "Current Order", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_displayOrderMenuItemActionPerformed

